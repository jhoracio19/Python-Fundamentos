
# Truthy (Valores verdaderos)
print(bool(True))
print(bool(1))
print(bool(123))
print(bool(1.1))
print(bool(-1))
print(bool(1j))
print(bool('Hola'))

# Falsey (Valores falsos)
# Valores que sean 0 o que esten vacios
print(bool(False))
print(bool(0))
print(bool(0.0))
print(bool(0j))
print(bool(''))
print(bool(None))
