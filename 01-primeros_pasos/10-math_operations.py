
# Suma
print(2 + 2) #4

# Resta
print(4 - 2) # 2

# Multiplicacion 
print(4 * 2) #8

# Division
print(2/4) #0.5

# Otras operaciones
print(2 ** 3) # Sacar potencia 8 
print(5//4) # Division sin decimal

# Modulo (lo que sobra de la division: residuo)
print(6%4) #2

# Redondear : round()
round(3.1)
print(round(3.1))
print(round(3.9))

# Valor absoluto : abs()
print(abs(20))
